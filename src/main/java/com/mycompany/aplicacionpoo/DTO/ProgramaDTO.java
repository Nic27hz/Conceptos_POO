package com.mycompany.aplicacionpoo.DTO;

import java.util.Date;

public class ProgramaDTO {
    private double id;
    private String nombre;
    private double duracion;
    private Date registro;
    private FacultadDTO facultad;

    public ProgramaDTO() {}

    public ProgramaDTO(double id, String nombre, double duracion, Date registro, FacultadDTO facultad) {
        this.id = id;
        this.nombre = nombre;
        this.duracion = duracion;
        this.registro = registro;
        this.facultad = facultad;
    }

    public double getId() {
        return id;
    }

    public void setId(double id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public double getDuracion() {
        return duracion;
    }

    public void setDuracion(double duracion) {
        this.duracion = duracion;
    }

    public Date getRegistro() {
        return registro;
    }

    public void setRegistro(Date registro) {
        this.registro = registro;
    }

    public FacultadDTO getFacultad() {
        return facultad;
    }

    public void setFacultad(FacultadDTO facultad) {
        this.facultad = facultad;
    }
}
